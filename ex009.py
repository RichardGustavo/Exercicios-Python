# Como utilizar transformação em string

nome = '   Richard Gustavo Gonçalves'

print(nome.replace('Richard','Alice')) # - Aqui você vai trocar qualquer coisa que queira por outra, lembrando que aqui estamos tratando de strings.
print(nome.upper()) # - Aqui você vai deixar toda a sua variável em maiúsculo.
print(nome.lower()) # - Aqui você vai deixar toda sua variável em minúsculo.
print(nome.capitalize()) # - Joga todos os caracteres em minúsculo, com exceção da primeira letra.
print(nome.title()) # - Aqui ele analisa quantas palavras tem na string através de cada espaço e deixa a primeira letra de cada palvra em maiúsculo.
print(nome.strip()) # - Aqui ele remove todos os espaços inúteis no começo e no fim da sua frase.
print(nome.rstrip()) # - Aqui voce remove todo espaço vazio do lado direito da frase.
print(nome.lstrip()) # - Aqui voce remove todo espaço vazio do lado esquerdo da frase.
